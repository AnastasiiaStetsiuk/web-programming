const task_3 = n => {
    // якщо n - не натуральне число, повертаємо помилку
    if (n < 0) {
        return 'число має бути натуральним'
    }

    // якщо n - 0 або 1, повертаємо 1 (умова зупинки рекурсії)
    if (n === 0 || n === 1) {
        return 1
    }

    // повертаємо результат множення n на результат рекурсивного виклику функції
    return n * task_3(n - 1)
}

module.exports = task_3
